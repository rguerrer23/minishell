#include "../inc/minishell.h"

int	has_pipe(char **cmd)
{
	int	i;

	i = 0;
	while (cmd[i])
	{
		if (ft_strcmp(cmd[i], "|") == 0)
			return (1);
		i++;
	}
	return (0);
}
